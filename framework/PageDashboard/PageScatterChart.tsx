import { Chart, ChartAxis, ChartScatter, ChartVoronoiContainer } from '@patternfly/react-charts';
import { ReactNode, useMemo, useState } from 'react';
import { PageChartContainer } from './PageChartContainer';

export function PageScatterChart() {
  const [padding] = useState(() => ({
    bottom: 1,
    left: 40,
    right: 5, // Adjusted to accommodate legend
    top: 5,
  }));

  const data = useMemo(() => {
    const anomalies: IHostPlot[] = [];
    const regular: IHostPlot[] = [];
    let index = 1;
    for (const host of hosts) {
      host.date = new Date(host.x);
      host.x = index++;
      if (host.anomaly) {
        anomalies.push(host);
      } else {
        regular.push(host);
      }
    }
    return { anomalies, regular };
  }, []);

  return (
    <PageChartContainer>
      {(size) => (
        <Chart
          // title="Organization Jobs"
          // ariaDesc="Average number of pets"
          // ariaTitle="Stack chart example"
          containerComponent={
            <ChartVoronoiContainer
              // eslint-disable-next-line @typescript-eslint/restrict-template-expressions, @typescript-eslint/no-unsafe-member-access
              labels={({ datum }) => `${datum.name}: ${datum.y}`}
              constrainToVisibleArea
            />
          }
          // domainPadding={{ x: [30, 25] }}
          //   legendData={stats.orgsStats.map((org) => ({ name: org.name }))}
          // legendData={legendData.map((ld, i) => ({
          //   name: ld,
          //   symbol: {
          //     fill: colorScale[3 - i],
          //   },
          // }))}
          // legendOrientation="vertical"
          // legendPosition="right"
          // colorScale={['#4CB140', '#F4C145', '#EF9234', '#C9190B']}
          name="chart1"
          padding={padding}
          width={size.width}
          height={size.height}
          minDomain={{ y: 0 }}
          // animate={true}
        >
          <ChartAxis tickValues={[0]} tickFormat={['']} />
          <ChartAxis dependentAxis showGrid />
          <ChartScatter
            data={data.regular}
            groupComponent={<GroupComponent fill="#0066CC" />}
            dataComponent={<DataComponent size={2} />}
          />
          <ChartScatter
            data={data.anomalies}
            groupComponent={<GroupComponent fill="#C9190B" />}
            dataComponent={<DataComponent size={3} />}
          />
        </Chart>
      )}
    </PageChartContainer>
  );
}

function GroupComponent(props: { fill: string; children?: ReactNode; opacity?: number }) {
  return (
    <g fill={props.fill} opacity={props.opacity ?? 1}>
      {props.children}
    </g>
  );
}

function DataComponent(props: { x?: number; y?: number; size: number }) {
  return <circle cx={props.x} cy={props.y} r={props.size ?? 4}></circle>;
}

